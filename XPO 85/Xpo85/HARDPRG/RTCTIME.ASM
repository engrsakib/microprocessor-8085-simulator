
		CPU "8085.TBL"
		HOF "INT8"
CRLF:   EQU 048AH
NMOUT:  EQU 036CH
WR79:   EQU 015DH
CRONLY: EQU 0497H
        ORG 6290H
        CALL CRLF    ;CLEAR/REFRESH THE DISPLAY
 START: IN 44H       ;READ HOURS FROM I/P PORT 44H
        MOV E,A      ;HOURS ARE DIRECTLY IN BCDS
        MVI B,02H    ;DISPLAY HOURS (TWO DIGITS)
        CALL NMOUT   ;ON DISPLAY
        MVI A,20H    ;DISPLAY BLANK SPACE
        CALL WR79    ;ON THE DISPLAY
        IN 43H       ;READ MINUTES FROM I/P PORT 43H
        MOV E,A      ;IN BCD FORMAT
        MVI B,02H    ;DISPLAY MINUTES (TWO DIGITS)
        CALL NMOUT   ;IN BCD FORMAT
        MVI A,20H    ;DISPLAY BLANK SPACE
        CALL WR79    ;ON THE DISPLAY
        IN 42H       ;READ SECONDS FROM I/P PORT 42H
        MOV E,A      ;IN BCD FORMATS
        MVI B,02H    ;DISPLAY SECONDS (TWO DIGITS)
        CALL NMOUT   ;ON THE DISPLAY
        CALL CRONLY  ;BRING CURSER AT START POSITION
        JMP START    ;JUMP TO START.
