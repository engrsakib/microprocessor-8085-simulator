;8255 FLASHING LEDS
                CPU "8085.TBL"
		HOF "INT8"
CRLF:   EQU 048AH
CPURD:  EQU 10H
NMOUT:  EQU 036CH
DELAY:  EQU 0615H
CRONLY: EQU 0497H
        ORG 6065H
        CALL CRLF
RDKEY:  MVI A,8EH
        OUT CPURD
        LXI D,0000H
        CALL DELAY
        IN CPURD
        ORA A
        JP RDKEY
        ANI 7FH
        CALL CRONLY
        MVI B,02H
        MOV E,A
        CALL NMOUT
        JMP RDKEY
