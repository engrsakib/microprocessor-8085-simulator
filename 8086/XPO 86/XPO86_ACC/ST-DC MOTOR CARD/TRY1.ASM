        ;PROGRAM FOR THE STEPPER MOTOR ROTATION ANTICLOCKWISE
        ;NOTE:TO MAKE ROTATION OF STEPPER MOTOR FASTER 
		;MOVE VALUE IN CX REGISTER 4000H.
		STEPPER SEGMENT
		ASSUME CS:STEPPER
		CR55 EQU 8006H
		PORTA EQU 8000H
		ORG 0100H
   STRT:MOV CX,7500H ;COUNTER VALUE FOR DELAY ROUTINE
		MOV AL,80H    ;Init.Part A,B of 8255
		MOV DX,CR55   ;Port C as output port
		OUT DX,AL
		MOV AL,00H    
		MOV DX,PORTA  
		OUT DX,AL
  AGAIN:MOV AL,03H  ;1st phase code
        OUT DX,AL
		INT 0AAH    ;delay routine
		MOV AL,09H  ;2nd phase code
        OUT DX,AL   
		INT 0AAH
		MOV AL,0CH  ;3rd phase code
        OUT DX,AL
		INT 0AAH
		MOV AL,06H  ;4th phase code
        OUT DX,AL
		INT 0AAH
		JMP AGAIN   ;begin rotation again
        STEPPER ENDS
        END STRT
        